import math


def tambah(bil1, bil2):
    hasil = bil1+bil2
    print("Hasil Penjumlahan:", hasil)

def kurang(bil1, bil2):
    hasil = bil1 - bil2
    print("Hasil Pengurangan:", hasil)

def kali(bil1, bil2):
    hasil = bil1 * bil2
    print("Hasil Perkalian:", hasil)

def bagi(bil1, bil2):
    hasil = bil1 / bil2
    print("Hasil Pembagian:", hasil)

def pangkat(bil1, bil2):
    hasil = bil1 ^ bil2
    print("Hasil Perpangkatan:", hasil)

def modul(bil1, bil2):
    hasil = bil1 % bil2
    print("Hasil Modulus:", hasil)

def pembagian_bilbul(bil1, bil2):
    hasil = bil1 // bil2
    print("Hasil Pembagian bilangan bulat:", hasil)

def gabungan_operasi(bil1, bil2, bil3):
    hasil = (bil1 + bil2) * bil3
    print("Hasil Gabungan Operasi:", hasil)

def operasi_campuran(bil1, bil2, bil3, bil4):
    hasil = (bil1 - bil2) * bil3 + bil4
    print("Hasil Operasi Campuran:", hasil)

def akar_kuadrat(bil1):
    hasil = bil1 * 0.5
    print("Hasil Akar Kuadrat:", hasil)